            String checkUrl = TextField.getText();
            String newUrl = "";

            if (checkUrl.startsWith("http://")) {
                newUrl = checkUrl;
            }

            if (checkUrl.startsWith("www")) {
                newUrl = "http://" + checkUrl;
            } else {
                newUrl = checkUrl;
            }
            m.link = newUrl;
            Document d = Jsoup.connect(newUrl).get();
            jTextArea2.append(d.toString());
            jTextArea2.update(jTextArea2.getGraphics());
            t.start();
